    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            String MaLoai = txtmaloai.getText();
            String TenLoai = txttenloai.getText();
            if(loaiBUS.check(MaLoai))
            {
                JOptionPane.showMessageDialog(null, "Mã loaiách hàng đã tồn tại !!!");
                return;
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Thêm loaiách hàng thành công.");
            }
            Loai_DTO loaiDTO= new Loai_DTO(MaLoai,TenLoai);
            loaiBUS.addL(loaiDTO);
            cleanView();
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"Chưa nhập đầy đủ thông tin loaiách hàng !!!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        loaiBUS.getDSLoai();
        if(txtmaloai.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn loaiách hàng cần sửa !!!");
            return;
        }
        int j = JOptionPane.showConfirmDialog(null, "Xác nhận sửa!","",JOptionPane.YES_NO_OPTION);
        if(j == 0)
        {
            String MaLoai = txtmaloai.getText();
            String TenLoai = txttenloai.getText();
            Loai_DTO loaiDTO= new Loai_DTO(MaLoai,TenLoai);
            loaiBUS.setL(loaiDTO);
            JOptionPane.showMessageDialog(null, "Sửa thành công","Thành công",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        loaiBUS.getDSLoai();
        int manv = JOptionPane.showConfirmDialog(null,"Xác nhận xóa loaiách hàng!","Xóa loaiách hàng", JOptionPane.YES_NO_OPTION);
        if(manv == 0) {
            loaiBUS.deleteL(txtmaloai.getText());
        }
        cleanView();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        loaiBUS.getDSLoai();
        Loai_BUS loaiBUS = new Loai_BUS();
        loaiBUS.listL();
        model.setRowCount(0);
        loaiBUS.getList();
        int i=0;
        for(Loai_DTO loaiDTO : loaiBUS.dsl) {
            model.addRow(new Object[] {++i,loaiDTO.getMaLoai(),
                loaiDTO.getTenLoai(),});
        }
        cleanView();
    }//GEN-LAST:event_btnImportActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int i=table.getSelectedRow();
        if (i>=0){
            txtmaloai.setText(model.getValueAt(i, 1).toString());
            txttenloai.setText(model.getValueAt(i, 2).toString());

        }
    }//GEN-LAST:event_tableMouseClicked


